import pad from './pad'

test('pad', () => {
  expect(pad(0)).toBe('0')
  expect(pad(0, 5)).toBe('00000')
  expect(pad(123)).toBe('123')
  expect(pad(123, 2)).toBe('123')
  expect(pad(123, 5)).toBe('00123')
  expect(pad(0.123)).toBe('0')
  expect(pad(0.123, 0, 3)).toBe('0.123')
  expect(pad(0.123, 0, 5)).toBe('0.12300')
  expect(pad(0.123, 2)).toBe('00')
  expect(pad(0.123, 2, 3)).toBe('00.123')
  expect(pad(0.123, 2, 5)).toBe('00.12300')
  expect(pad(0.123, 5)).toBe('00000')
  expect(pad(0.123, 5, 3)).toBe('00000.123')
  expect(pad(0.123, 5, 5)).toBe('00000.12300')
  expect(pad(123.123)).toBe('123')
  expect(pad(123.123, 0, 3)).toBe('123.123')
  expect(pad(123.123, 0, 5)).toBe('123.12300')
  expect(pad(123.123, 2)).toBe('123')
  expect(pad(123.123, 2, 3)).toBe('123.123')
  expect(pad(123.123, 2, 5)).toBe('123.12300')
  expect(pad(123.123, 5)).toBe('00123')
  expect(pad(123.123, 5, 3)).toBe('00123.123')
  expect(pad(123.123, 5, 5)).toBe('00123.12300')
  expect(pad(-0)).toBe('0')
  expect(pad(-0, 5)).toBe('00000')
  expect(pad(-123)).toBe('-123')
  expect(pad(-123, 2)).toBe('-123')
  expect(pad(-123, 5)).toBe('-00123')
  expect(pad(-0.123)).toBe('0')
  expect(pad(-0.123, 0, 3)).toBe('-0.123')
  expect(pad(-0.123, 0, 5)).toBe('-0.12300')
  expect(pad(-0.123, 2)).toBe('00')
  expect(pad(-0.123, 2, 3)).toBe('-00.123')
  expect(pad(-0.123, 2, 5)).toBe('-00.12300')
  expect(pad(-0.123, 5)).toBe('00000')
  expect(pad(-0.123, 5, 3)).toBe('-00000.123')
  expect(pad(-0.123, 5, 5)).toBe('-00000.12300')
  expect(pad(-123.123)).toBe('-123')
  expect(pad(-123.123, 0, 3)).toBe('-123.123')
  expect(pad(-123.123, 0, 5)).toBe('-123.12300')
  expect(pad(-123.123, 2)).toBe('-123')
  expect(pad(-123.123, 2, 3)).toBe('-123.123')
  expect(pad(-123.123, 2, 5)).toBe('-123.12300')
  expect(pad(-123.123, 5)).toBe('-00123')
  expect(pad(-123.123, 5, 3)).toBe('-00123.123')
  expect(pad(-123.123, 5, 5)).toBe('-00123.12300')
})